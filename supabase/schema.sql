-- ── 1. PROFILES ──────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.profiles (
  id uuid NOT NULL DEFAULT gen_random_uuid(),
  first_name text,
  last_name text,
  address text,
  role text DEFAULT 'user_startup',
  CONSTRAINT profiles_pkey PRIMARY KEY (id),
  CONSTRAINT profiles_id_fkey FOREIGN KEY (id) REFERENCES auth.users (id)
);

-- ── 2. PROJECTS ──────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.projects (
  id bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  public_id uuid NOT NULL DEFAULT gen_random_uuid(),
  nom_projet text,
  region text,
  description text,
  is_entreprise boolean DEFAULT false,
  nom_entreprise text,
  siren text,
  activite text,
  adresse_siege text,
  effectif text,
  date_creation date,
  dirigeant text,
  user_id uuid,
  wenr_carbon_impact double precision,
  collaborator_count bigint,
  domaine text,
  livrable text,
  couts double precision,
  type_approche text,
  main_objectifs text,
  environmental_objectifs text,
  cible_service text,
  problematique text,
  referent_profil text,
  referent_projet text,
  accompagnateur_id uuid,
  project_type text,
  audit_done boolean DEFAULT false,
  audit_upload_url text,
  charte_ia_signed boolean DEFAULT false,
  charte_nr_signed boolean DEFAULT false,
  CONSTRAINT projects_pkey PRIMARY KEY (id),
  CONSTRAINT projects_public_id_key UNIQUE (public_id),
  CONSTRAINT projects_accompagnateur_id_fkey FOREIGN KEY (accompagnateur_id) REFERENCES public.profiles (id) ON DELETE SET NULL,
  CONSTRAINT projects_user_id_fkey FOREIGN KEY (user_id) REFERENCES auth.users (id)
);
CREATE INDEX IF NOT EXISTS idx_projects_accompagnateur_id ON public.projects USING btree (accompagnateur_id);
CREATE UNIQUE INDEX IF NOT EXISTS idx_projects_public_id ON public.projects (public_id);

-- ── 3. QUESTIONS ─────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.questions (
  id bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  numero integer,
  dimension text,
  thematique text,
  intitule text,
  niveau integer,
  reponse_0 text,
  reponse_1 text,
  reponse_2 text,
  reponse_3 text,
  reponse_4 text,
  dimension_en text,
  thematique_en text,
  intitule_en text,
  reponse_0_en text,
