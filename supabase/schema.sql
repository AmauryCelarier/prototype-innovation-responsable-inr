-- ── 7. RESILIENCE_RESPONSES ──────────────────────────────────
CREATE TABLE IF NOT EXISTS public.resilience_responses (
  id uuid NOT NULL DEFAULT gen_random_uuid(),
  project_id bigint,
  critere_id text,
  score integer NOT NULL,
  updated_at timestamp with time zone NOT NULL DEFAULT timezone('utc'::text, now()),
  CONSTRAINT resilience_responses_pkey PRIMARY KEY (id),
  CONSTRAINT project_critere_unique UNIQUE (project_id, critere_id),
  CONSTRAINT resilience_responses_project_id_fkey FOREIGN KEY (project_id) REFERENCES public.projects (id) ON DELETE CASCADE
);

-- ── TRIGGER : création automatique du profil à l'inscription ─
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS trigger LANGUAGE plpgsql SECURITY DEFINER AS $$
BEGIN
  INSERT INTO public.profiles (id, role)
  VALUES (NEW.id, 'user_startup')
  ON CONFLICT (id) DO NOTHING;
  RETURN NEW;
END;
$$;

CREATE OR REPLACE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE FUNCTION public.handle_new_user();

-- ── HELPER RLS ───────────────────────────────────────────────
CREATE OR REPLACE FUNCTION public.is_admin()
RETURNS boolean LANGUAGE sql SECURITY DEFINER STABLE AS $$
  SELECT EXISTS (
    SELECT 1 FROM public.profiles
    WHERE id = auth.uid() AND role = 'admin'
  );
$$;

