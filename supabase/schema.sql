-- ── 4. RESPONSES ─────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.responses (
  id uuid NOT NULL DEFAULT gen_random_uuid(),
  user_id uuid NOT NULL,
  question_id text,
  score text,
  updated_at timestamp with time zone DEFAULT now(),
  project_id bigint,
  nom text,
  prenom text,
  adresse text,
  step_id integer,
  category text,
  CONSTRAINT responses_pkey PRIMARY KEY (id),
  CONSTRAINT responses_unique_project_step_question UNIQUE (project_id, step_id, question_id),
  CONSTRAINT responses_user_id_fkey FOREIGN KEY (user_id) REFERENCES auth.users (id),
  CONSTRAINT responses_project_id_fkey FOREIGN KEY (project_id) REFERENCES public.projects (id)
);

-- ── 5. PROJECT_TRAININGS ─────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.project_trainings (
  id uuid NOT NULL DEFAULT extensions.uuid_generate_v4(),
  project_id bigint,
  user_id uuid,
  training_id text NOT NULL,
  completed boolean DEFAULT false,
  CONSTRAINT project_trainings_pkey PRIMARY KEY (id),
  CONSTRAINT project_trainings_project_id_training_id_key UNIQUE (project_id, training_id),
  CONSTRAINT project_trainings_project_id_fkey FOREIGN KEY (project_id) REFERENCES public.projects (id) ON DELETE CASCADE,
  CONSTRAINT project_trainings_user_id_fkey FOREIGN KEY (user_id) REFERENCES auth.users (id)
);

-- ── 6. RESILIENCE ────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.resilience (
  id bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  dimension text,
  dimension_id text,
  critere text,
  description text,
  intention text,
  moyen text,
  resultat text,
  critere_en text,
  description_en text,
  intention_en text,
  moyen_en text,
  resultat_en text,
  CONSTRAINT resilience_pkey PRIMARY KEY (id)
